Map::Map()
{
	dirt = TextureManager::LoadTexture("assets/TinySwords/Terrain/Ground/Tilemap_Flat.png");
	water = TextureManager::LoadTexture("assets/TinySwords/Terrain/Water/Water.png");

	loadMap(lvl1);
	
	src.x = src.y = 0;

	src.w = dst.w = 128;
	src.h = dst.h = 128;

	dst.x = dst.y = 0;
}

Map::~Map()
{

}

void Map::loadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			map[row][col] = arr[row][col];
		}
	}
}

void Map::drawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			type = map[row][col];

			dst.y = row * 32;
			dst.x = col * 32;

			switch (type)
			{
			case 0:
				TextureManager::DrawTexture(dirt, src, dst);
				break;
			case 1:
				TextureManager::DrawTexture(water, src, dst);
				break;
			default:
				break;
			}
		}
	}
}